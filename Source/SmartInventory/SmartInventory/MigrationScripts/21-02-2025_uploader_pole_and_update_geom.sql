CREATE OR REPLACE FUNCTION public.fn_uploader_insert_pole(
	p_uploadid integer,
	p_batchid integer)
    RETURNS integer
    LANGUAGE 'plpgsql'
    COST 100
    VOLATILE PARALLEL UNSAFE
AS $BODY$

declare
REC RECORD;
v_current_system_id integer;
v_parent_system_id integer;
v_parent_network_id character varying;
v_parent_entity_type character varying;
v_network_code character varying;
v_sequence_id integer;
v_latitude double precision;
v_longitude double precision;
v_network_id_type character varying;
v_network_code_seperator character varying;
v_auto_character_count integer;
v_network_name character varying;
v_geom text;
BEGIN
-- GET NETWORK ID TYPE (AUTO/MANUAL) AND NETWORK CODE SEPERATOR FROM LAYER DETAILS..
select network_id_type,network_code_seperator into v_network_id_type,v_network_code_seperator from layer_details where upper(layer_name)='POLE';

-- INSERT BATCH WISE RECORDS..
FOR REC IN select * from temp_du_pole where upload_id=p_uploadid and is_valid=true and batch_id=p_batchid and is_processed = false
LOOP
BEGIN
v_latitude:=rec.latitude::double precision;
v_longitude:=rec.longitude::double precision;
v_sequence_id:=0;
v_network_name:=rec.pole_name;

-- IF MANUAL THEN ACCEPT THE NETWORK CODE ENTERED BY USER.
if (v_network_id_type='M' and coalesce(REC.network_id,'')!='')
then
v_network_code=rec.parent_network_id||v_network_code_seperator||rec.network_id;
elsif((v_network_id_type='A' and coalesce(REC.network_id,'')!=''))
then
v_network_code=rec.parent_network_id||v_network_code_seperator||rec.network_id;
select (length(network_code_format)-length(replace(network_code_format,RIGHT(network_code_format, 1),''))) into v_auto_character_count
from vw_layer_mapping where upper(child_layer_name)=upper('Pole') and upper(parent_layer_name)=UPPER(REC.PARENT_ENTITY_TYPE) and is_used_for_network_id=true;
v_sequence_id:=RIGHT(rec.network_id, v_auto_character_count);
else
-- GET NETWORK CODE & PARENT DETAILS..

select o_p_system_id,o_p_network_id,o_p_entity_type,message,o_sequence_id
from fn_get_clone_network_code('Pole', 'Point',''||v_longitude||' '||v_latitude||'', rec.parent_system_id, rec.parent_entity_type) into
v_parent_system_id,v_parent_network_id,v_parent_entity_type,v_network_code,v_sequence_id;
end if;

IF(coalesce(v_network_name,'')='')
then
v_network_name=v_network_code;
END IF;
--INSERT DATA IN MAIN TABLE
select case when (select value from global_settings where key= 'IsCustomProjectionAllowed')::integer=1 then ST_TRANSFORM(ST_GEOMFROMTEXT('POINT('||v_longitude||' '||v_latitude||')',(select value from global_settings where key= 'customProjection')::integer),4326) else ST_GEOMFROMTEXT('POINT('||v_longitude||' '||v_latitude||')',4326)
END into v_geom;

insert into att_details_pole(network_id,pole_name,latitude,longitude,province_id,
region_id, address,pole_no,pole_height,specification,category,subcategory1,subcategory2,subcategory3,
item_code,vendor_id,status,created_by,created_on,network_status,pole_type,parent_system_id,
parent_network_id,parent_entity_type,sequence_id,type,brand,model,construction,activation,accessibility,
ownership_type, source_ref_type, source_ref_id, remarks,origin_from,origin_Ref_id,origin_Ref_code,
origin_Ref_description,request_ref_id,requested_by,request_approved_by,bom_sub_category,st_x,st_y,gis_design_id,subarea_id, third_party_vendor_id)
select v_network_code,v_network_name,v_latitude,v_longitude,rec.province_id,rec.region_id,rec.address,rec.pole_no,
rec.pole_height,rec.specification,
rec.category,rec.subcategory1,rec.subcategory2,rec.subcategory3,rec.item_code,rec.vendor_id,'A',rec.created_by,now()
,coalesce(rec.network_status,'P'),rec.pole_type,
rec.parent_system_id,rec.parent_network_id,REC.PARENT_ENTITY_TYPE,v_sequence_id,0,0,0,0,0,0,rec.ownership_type, 'DU', p_uploadid,
rec.remarks,rec.origin_from,rec.origin_Ref_id,rec.origin_Ref_code,rec.origin_Ref_description,rec.request_ref_id,
rec.requested_by,rec.request_approved_by,'Proposed',rec.st_x,rec.st_y,rec.gis_design_id,rec.subarea_id,rec.own_vendor_id returning system_id into v_current_system_id;

--INSERT DATA IN POINT TABLE
insert into point_master (system_id,entity_type,latitude,longitude,approval_flag,sp_geometry,approval_date,creator_remark,
approver_remark,created_by,approver_id,common_name,db_flag,network_status,display_name,st_x,st_y)
select v_current_system_id,'Pole',v_longitude,v_latitude, 'A', ST_GEOMFROMTEXT('POINT('||v_longitude||' '||v_latitude||')', 4326),
now(),'NA','NA',rec.created_by,0,v_network_code,rec.upload_id,coalesce(rec.network_status,'P'),fn_get_display_name(v_current_system_id,'Pole'),rec.st_x,rec.st_y;

-- UPDATE STATUS INTO TEMP TABLE..
update temp_du_pole set is_processed=true where system_id=rec.system_id;

perform (fn_geojson_update_entity_attribute(v_current_system_id::integer,'Pole'::character varying ,rec.province_id::integer,0,false));

END;
END LOOP;
return 1;
END;
$BODY$;



CREATE OR REPLACE FUNCTION public.fn_uploader_update_vendor_specification(
	p_upload_id integer,
	p_entity_type character varying)
    RETURNS integer
    LANGUAGE 'plpgsql'
    COST 100
    VOLATILE PARALLEL UNSAFE
AS $BODY$

declare
result int:=0;
v_layer_id integer;
sql character varying;
v_isvirtual_port_allowed boolean;
v_layer_geom_type character varying;
v_data_upload_table character varying;
V_LAYER_TABLE CHARACTER VARYING;
v_is_template_required boolean;
v_count_check integer;
begin

v_count_check := 0;
v_is_template_required:=false;
select is_virtual_port_allowed,layer_id,geom_type,data_upload_table,LAYER_TABLE,is_template_required 
into v_isvirtual_port_allowed,v_layer_id,v_layer_geom_type,v_data_upload_table,V_LAYER_TABLE,v_is_template_required  
from layer_details where lower(layer_name)=lower(p_entity_type);

if(v_is_template_required)
then

EXECUTE format('SELECT count(1) FROM %I t WHERE is_valid=true AND t.upload_id=$1 AND upper(t.ownership_type) = ''THIRD PARTY'' ',
        v_data_upload_table ) INTO v_count_check USING p_upload_id;

--UPDATE THE DEFAULT SPECIFICATION:- Generic
IF EXISTS (SELECT column_name FROM information_schema.columns WHERE UPPER(TABLE_NAME)=UPPER(v_data_upload_table) AND UPPER(COLUMN_NAME)='SPECIFICATION') THEN 
	sql:='update '||v_data_upload_table||' t set specification=''Generic'' 
	where coalesce(t.specification,'''')='''' and is_valid=true and t.upload_id='||p_upload_id||'';
	raise info 'SQL:% ',sql;
	execute sql; 
END IF;

--UPDATE THE DEFAULT VENDOR_NAME:- Generic
IF EXISTS (SELECT column_name FROM information_schema.columns WHERE UPPER(TABLE_NAME)=UPPER(v_data_upload_table) AND UPPER(COLUMN_NAME)='VENDOR_NAME') THEN 
	sql:='update '||v_data_upload_table||' t set vendor_name=''Generic''
	where coalesce(t.vendor_name,'''')='''' and is_valid=true and t.upload_id='||p_upload_id||'';			
	raise info 'SQL:% ',sql;
	execute sql; 
END IF;

--UPDATE THE SPECIFICATION

sql:= 'update '||v_data_upload_table||' i set specification_id=v.id, audit_item_master_id =v.audit_id from item_template_master v 
inner join vendor_master vnd on v.vendor_id=vnd.id
where upper(TRIM(v.specification))=upper(TRIM(i.specification)) and upper(i.vendor_name)= upper(vnd.name) and i.is_valid=true and v.layer_id='||v_layer_id||' and i.upload_id='||p_upload_id||'';
execute sql;
raise info 'SQL1:% ',sql;
--VALIDATE THE SPECIFICATION					
sql:= 'update '||v_data_upload_table||' i set is_valid=false, error_msg =''Specification not Found!'' 
where i.specification_id=0 and i.is_valid=true and i.upload_id='||p_upload_id||';';
execute sql;

--UPDATE VENDOR ID
sql:= 'update '||v_data_upload_table||' t set vendor_id=v.id from vendor_master v join item_template_master i on 
v.id=i.vendor_id and layer_id='||v_layer_id||' where  upper(t.vendor_name)= upper(v.name) and i.id=t.specification_id and t.upload_id='||p_upload_id||'';
raise info 'SQL:% ',sql;
execute sql;
--VALIDATE VENDOR ID
sql:= 'update '||v_data_upload_table||' i set is_valid=false, error_msg =''Vendor not Found for this Specification!''  where  i.vendor_id =0 and i.is_valid=true and i.upload_id='||p_upload_id||'';
execute sql;

--UPDATE THIRD PARTY VENDOR ID

IF(v_count_check >0) THEN
sql:= 'update '||v_data_upload_table||' t set own_vendor_id=v.id, ownership_type = ''Third Party''  from vendor_master v
where  upper(t.ownership_type_vendor_name)= upper(v.name) and upper(v.type) = ''THIRD PARTY'' and  t.upload_id='||p_upload_id||' ';
raise info 'SQL:% ',sql;
execute sql;

--VALIDATE Third party VENDOR ID
sql:= 'update '||v_data_upload_table||' i set is_valid=false, error_msg =''Third Party/IRU Vendor not Found!''  where  i.own_vendor_id =0 and i.is_valid=true and i.upload_id='||p_upload_id||'';
execute sql;

END IF;


--UPDATE OTHER DETAILS FROM ITEM TEMPLATE MASTER
SQL:=' update '||v_data_upload_table||' t set category='''||p_entity_type||''' , subcategory1=m.subcategory_1 , subcategory2=m.subcategory_2, subcategory3= m.subcategory_3,item_code=m.code ';

--IF EXISTS(SELECT  1 FROM  information_schema.columns WHERE   UPPER(table_name) = UPPER(v_data_upload_table) 
--AND  UPPER(column_name) = UPPER('NO_OF_INPUT_PORT') 
--AND UPPER(column_name) = UPPER('NO_OF_OUTPUT_PORT')
--AND UPPER(column_name) = UPPER('NO_OF_PORT'))
if(SELECT  count(*) FROM  information_schema.columns WHERE   UPPER(table_name) = UPPER(v_data_upload_table) and   UPPER(column_name) in ('NO_OF_INPUT_PORT', 'NO_OF_OUTPUT_PORT','NO_OF_PORT'))=3 
THEN
	SQL:=SQL||' ,no_of_input_port=m.no_of_input_port,no_of_output_port=m.no_of_output_port,no_of_port=m.no_of_port ';
elsif(upper(p_entity_type)='CABLE')
then
	SQL:=SQL||' ,total_core=m.other::integer ';
	--no_of_tube=1,core_per_tube=(other filed from item template master),total_core=(other filed from item template master)
END IF;

SQL:=SQL||' from item_template_master m where t.is_valid=true and t.specification_id=m.id and t.vendor_id=m.vendor_id and m.layer_id='||v_layer_id||' and upload_id='||p_upload_id||'';
raise info 'SQL:%',SQL;
execute sql;

if(upper(p_entity_type)='CABLE')
then
SQL:='update '||v_data_upload_table||' set no_of_tube=1,core_per_tube=total_core
where coalesce(no_of_tube,0)=0 and coalesce(core_per_tube,0)=0 and is_valid=true and upload_id='||p_upload_id||'';
execute SQL;
	
SQL:='update '||v_data_upload_table||' set is_valid=false,error_msg=''Multiply of no_of_tube and core_per_tube should be equal to total_core!'' 
where (coalesce(no_of_tube,0)*coalesce(core_per_tube,0))!=total_core and is_valid=true and upload_id='||p_upload_id||'';
execute SQL;	
end if;	
end if;
result:=1;
return result;

end;
$BODY$;



